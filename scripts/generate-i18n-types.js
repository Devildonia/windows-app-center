import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);

const localesDir = path.resolve(__dirname, '../public/locales');
const outputFilePath = path.resolve(__dirname, '../js/services/i18n-keys.ts');

try {
    const enLocalesPath = path.join(localesDir, 'en.json');
    if (!fs.existsSync(enLocalesPath)) {
        console.error(`Error: Source file ${enLocalesPath} not found.`);
        process.exit(1);
    }

    const enContent = fs.readFileSync(enLocalesPath, 'utf8');
    const locales = JSON.parse(enContent);
    const keys = Object.keys(locales).sort();

    const outputContent = `// This file is auto-generated by scripts/generate-i18n-types.js. Do not edit directly.
export type TranslationKey =
${keys.map(k => `    | "${k}"`).join('\n')};
`;

    fs.writeFileSync(outputFilePath, outputContent, 'utf8');
    console.log(`[i18n-types] Successfully generated ${keys.length} keys to ${outputFilePath}`);
} catch (error) {
    console.error('[i18n-types] Failed to generate types:', error);
    process.exit(1);
}
